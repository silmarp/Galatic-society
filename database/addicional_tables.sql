-- Table log
CREATE TABLE LOG_TABLE (
    USERID CHAR(16) NOT NULL,
    TIMESTAMP TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    MESSAGE VARCHAR2(4000) NOT NULL
);
